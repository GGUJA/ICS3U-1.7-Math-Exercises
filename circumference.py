# Ask the user for input
circle_radius = float(input("Enter radius: "))
pi = 3.14

# Calculate circumference by multiplying circle radius with pi and 2
circumference = 2 * circle_radius * pi

# Output result
print("Result: circumference is: " + str(circumference))
